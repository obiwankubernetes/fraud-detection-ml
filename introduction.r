### Group Project for TO628 @ UMich Ross

### I) Business Problem

Each year, there're around 21.84 billion lose in credit/debit card fraud around the golbe. It is important that credit card companies are able to recognize fraudulent credit card transactions to protect their customers from charges for items that they did not purchase. Our team attempts to design a model to predict the credit/debit card fraud. By doing this, we will be able to help financial institutions better detect fraud and help both the institution and customers to secure their property.

### II) Data Set Selected

The data set we selected can be found at Kaggle: https://www.kaggle.com/mlg-ulb/creditcardfraud. The dataset contains an anonymized European credit card transactions in September 2013, including 31 columns and 284,807 rows.
